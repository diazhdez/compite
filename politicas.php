<center><h1> Políticas y privacidad</h1></center>
<br><br>    
<p>Fecha de vigencia: 1 - Agosto - 2023</p>

<p>Introducción</p>

<p>Gracias por utilizar el sistema "COMPITE". Valoramos la confianza que depositas en nosotros al proporcionarnos tus datos personales. Nos comprometemos a proteger tu privacidad y a utilizar tus datos de manera responsable. Esta Política de Privacidad describe cómo recopilamos, utilizamos, almacenamos y protegemos la información personal que nos proporcionas. Al acceder y utilizar nuestro sistema, aceptas las prácticas descritas en esta Política de Privacidad.</p>

<p>Recopilación de Información</p>

<p>Datos personales recopilados: Recolectamos los siguientes datos personales de los usuarios del sistema:
</p>
<p>Nombre completo: Para identificarte de manera única y personalizar la experiencia en el sistema.
Correo electrónico: Para facilitar la comunicación contigo y enviarte notificaciones relevantes.
Empresa en la que trabajan: Para brindar servicios y funciones específicas relacionadas con la empresa.
Formas de recopilación: La información se recopila mediante formularios de registro, cuestionarios, interacciones con el sistema y comunicación directa contigo.
</p>
<p>Uso de la Información
</p>
<p>Utilización de los datos: Utilizamos la información recopilada para los siguientes fines:
</p>
<p>Procesar y gestionar tu registro en el sistema.
Brindar acceso y funcionalidades personalizadas.
Facilitar la comunicación contigo y responder a tus consultas.
Proporcionar servicios y funciones específicas relacionadas con la empresa en la que trabajas.
Realizar análisis internos y mejoras del sistema.
Cumplir con nuestras obligaciones legales y regulatorias.
Compartir información: No compartiremos tus datos personales con terceros, a menos que sea necesario para proporcionar los servicios requeridos por el sistema, o cuando la ley así lo exija.
</p>
<p>Seguridad de los Datos
</p>
<p>Seguridad: Implementamos medidas de seguridad técnicas y organizativas para proteger tus datos personales contra el acceso no autorizado, la divulgación o la alteración. Sin embargo, ninguna transmisión de datos por Internet o sistema de almacenamiento es completamente segura, por lo que no podemos garantizar su absoluta seguridad.
Derechos del Usuario
</p>
<p>Acceso y corrección: Puedes acceder, corregir o actualizar tus datos personales a través de los ajustes de tu cuenta en el sistema.
</p>
<p>Eliminación de datos: Si deseas que eliminemos tus datos personales de nuestro sistema, contáctanos a través de [correo electrónico o formulario de contacto] y procederemos según lo establecido en la ley aplicable.
</p>
<p>Cambios en la Política de Privacidad
</p>
<p>Modificaciones: Nos reservamos el derecho de modificar esta Política de Privacidad en cualquier momento. Te informaremos sobre los cambios significativos enviándote una notificación o mediante publicación en el sistema. Te recomendamos que revises periódicamente esta Política para estar al tanto de las prácticas de privacidad vigentes.
</p>
<p>Contacto
</p>
<p>Si tienes preguntas o inquietudes sobre esta Política de Privacidad, contáctanos a través de [correo electrónico o formulario de contacto].
</p>
<p>Gracias por confiar en [nombre del sistema]. Estamos comprometidos a proteger tu privacidad y a proporcionarte una experiencia segura y valiosa en nuestro sistema.
</p>
